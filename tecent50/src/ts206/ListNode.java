package tecent50.src.ts206;

/**
 * @Description 206. 反转链表
 * 给定单链表的头节点 head ，请反转链表，并返回反转后的链表。
 * @Author liq
 * @Date 2022/05/12
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
